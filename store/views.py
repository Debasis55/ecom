def index(request):
    products = Product.objects.all()

    if request.user.is_authenticated:
        customer = request.user.customer
        order, created = Order.objects.get_or_create(customer=customer)
        items = order.orderitem_set.all
        cartItems = order.get_cart_items
    else:
        items = []
        order = {'get_cart_total': 0, 'get_cart_items': 0}
        cartItems = order['get_cart_items']

    context = {'products': products, 'cartItems': cartItems}
    return render(request, "store/index.html", context)


def searchMatch(query, item):
    if query in item.product_desc.lower() or query in item.product_name.lower() or query in item.product_category.lower():
        return True
    else:
        return False


def search(request):
    products = Product.objects.all()
    query = request.GET.get('search')

    if request.user.is_authenticated:
        customer = request.user.customer
        order, created = Order.objects.get_or_create(customer=customer)
        items = order.orderitem_set.all
        cartItems = order.get_cart_items
        prod = [item for item in products if searchMatch(query, item)]
    else:
        items = []
        order = {'get_cart_total': 0, 'get_cart_items': 0}
        cartItems = order['get_cart_items']

    context = {'products': prod, 'cartItems': cartItems}
    return render(request, "store/index.html", context)


